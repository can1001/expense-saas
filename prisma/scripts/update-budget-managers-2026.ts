async function main() {
  console.log('🚀 2026년 예산 담당자 업데이트 시작...\n');

  // 2026년 BudgetDetailYear 데이터 조회 (관련 정보 포함)
  const budgetYears = await prisma.budgetDetailYear.findMany({
    where: { year: 2026 },
    include: {
      budgetDetail: {
        include: {
          subcategory: {
            include: { category: true }
          },
          departmentDetails: {
            include: {
              department: {
                include: { committee: true }
              }
            }
          }
        }
      },
      manager: true
    }
  });

  console.log(`📊 총 ${budgetYears.length}개의 2026년 예산 항목 발견\n`);

  let updateCount = 0;
  let skipCount = 0;
  const updates: Array<{ detail: string; oldManager: string; newManager: string }> = [];

  for (const by of budgetYears) {
    const detail = by.budgetDetail;
    const dept = detail.departmentDetails[0]?.department;
    const committee = dept?.committee;

    if (!committee || !dept) {
      skipCount++;
      continue;
    }

    const category = detail.subcategory.category.name;
    const subcategory = detail.subcategory.name;
    const detailName = detail.name;

    // 키 생성하여 Excel 매핑에서 찾기
    const key = `${committee.name}|${dept.name}|${category}|${subcategory}|${detailName}`;
    const newManagerName = excelManagerMap[key];

    if (newManagerName && userIds[newManagerName]) {
      const newManagerId = userIds[newManagerName];
      const currentManagerName = by.manager?.username || '(없음)';

      // 이미 같은 담당자면 스킵
      if (by.managerId === newManagerId) {
        skipCount++;
        continue;
      }

      // 업데이트 실행
      await prisma.budgetDetailYear.update({
        where: { id: by.id },
        data: { managerId: newManagerId }
      });

      updates.push({
        detail: `${committee.name} > ${dept.name} > ${detailName}`,
        oldManager: currentManagerName,
        newManager: newManagerName
      });
      updateCount++;
    } else {
      skipCount++;
    }
  }

  console.log('=' .repeat(80));
  console.log('📝 업데이트 내역:');
  console.log('=' .repeat(80));

  for (const u of updates) {
    console.log(`✅ ${u.detail}`);
    console.log(`   ${u.oldManager} → ${u.newManager}`);
  }

  console.log('\n' + '=' .repeat(80));
  console.log(`✅ 업데이트 완료: ${updateCount}개`);
  console.log(`⏭️ 스킵됨: ${skipCount}개`);
  console.log('=' .repeat(80));
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
